export function StudentSidebar({
  unreadCount = 0,
  branding,
}: {
  unreadCount?: number;
  branding?: FirmBranding;
}) {
  const pathname = usePathname();

  return (
    <aside className="fixed inset-y-0 left-0 z-50 w-64 bg-sidebar-bg">
      <div className="flex h-16 items-center px-6">
        <Link href="/student-dashboard" className="flex items-center gap-2">
          {branding?.logoUrl ? (
            /* Firm white-label logo (fix plan 9.2) */
            /* eslint-disable-next-line @next/next/no-img-element */
            <img
              src={branding.logoUrl}
              alt={branding.firmName ?? "Firm logo"}
              className="max-h-9 max-w-[13rem] rounded"
            />
          ) : (
            <Wordmark dark />
          )}
        </Link>
      </div>
      <nav className="mt-2 px-3 space-y-1">
        {navigation.map((item) => {
          const isActive =
            pathname === item.href || pathname.startsWith(item.href + "/");
          return (
            <Link
              key={item.name}
              href={item.href}
              className={cn(
                "flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-colors",
                isActive
                  ? "bg-sidebar-active text-white"
                  : "text-sidebar-text hover:bg-sidebar-hover"
              )}
            >
              <item.icon className="h-5 w-5 shrink-0" />
              <span className="flex-1">{item.name}</span>
              {item.name === "Messages" && unreadCount > 0 && (
                <span
                  className="ml-auto inline-flex min-w-5 items-center justify-center rounded-full bg-danger-500 px-1.5 py-0.5 text-[10px] font-semibold text-white"
                  aria-label={`${unreadCount} unread messages`}
                >
                  {unreadCount > 99 ? "99+" : unreadCount}
                </span>
              )}
            </Link>
          );
        })}
      </nav>
    </aside>
  );
}

