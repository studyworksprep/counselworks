-- Update existing colleges
UPDATE colleges c
SET
    usnews_national_rank = r.national_rank,
    usnews_business_rank = r.business_rank,
    usnews_liberal_arts_rank = r.liberal_arts_rank
FROM _rankings r
WHERE c.scorecard_id = r.scorecard_id;

-- Insert colleges that don't already exist
INSERT INTO colleges (name, slug, country, scorecard_id, usnews_national_rank, usnews_business_rank, usnews_liberal_arts_rank)
SELECT r.school_name, r.slug, 'US', r.scorecard_id, r.national_rank, r.business_rank, r.liberal_arts_rank
FROM _rankings r
WHERE NOT EXISTS (
    SELECT 1 FROM colleges c WHERE c.scorecard_id = r.scorecard_id
)
ON CONFLICT (slug) DO UPDATE SET
    scorecard_id = EXCLUDED.scorecard_id,
    usnews_national_rank = EXCLUDED.usnews_national_rank,
    usnews_business_rank = EXCLUDED.usnews_business_rank,
    usnews_liberal_arts_rank = EXCLUDED.usnews_liberal_arts_rank;

